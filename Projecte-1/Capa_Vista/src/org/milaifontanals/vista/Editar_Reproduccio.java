    private void ConfirmarEdicio(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConfirmarEdicio
        try {
            
            //Agafem el client seleccionat
            Client cli_nou = null;
            cli_nou = cBD.getClient(jComboBox1.getSelectedItem().toString());

            //Agafem la data seleccioanada

            int dia_nou=(Integer)dia.getValue();
            int mes_nou=(Integer)mes.getValue()-1;
            int any_nou=(Integer)any.getValue()-1900;

            Date d_rep = null;
            
            if(dia_nou >=1 && mes_nou >=1 && any_nou >=1500){
            d_rep= new Date(any_nou,mes_nou,dia_nou);
            
            //Agafem el producte indicat
            Prod_Rep prod = new Prod_Rep (ProducteEditar.getText());

            //Cridem al constructor de Prova per poder editar la  reproduccio
            Reproducció nou = new Reproducció(d_rep,cli_nou,prod);
            

            
            //Cridem al metode de la capa de persistencia que ens fa el update
            cBD.editarReproduccio(nou,editar);
            }else{
                JOptionPane.showMessageDialog(this, "El dia i el mes han de ser major o igual a 1 i el any major o igual a 1500");
            }
            
        } catch (GestorBDEmpresaException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(),"Ups, ha hagut un error!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ConfirmarEdicio

