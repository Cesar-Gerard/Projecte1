    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        //Omplim el combobox dels usuaris
        List<Client> est = new ArrayList<Client>();
        
        
        try {
            est = cBD.LlistaClients();
            for(int i =0; i<est.size();i++){
                jComboBox1.addItem(est.get(i).getNom());
            }
        } catch (GestorBDEmpresaException ex) {
           JOptionPane.showMessageDialog(null, ex.getMessage(),"Ups, ha hagut un error!", JOptionPane.ERROR_MESSAGE);
        }
        
        
        
        
        //Fixem els valors de la reproducció original
        ClientOriginal.setText(editar.getIdClient().getNom());
        DataOriginal.setText(editar.getRep_mt());
        ProducteOriginal.setText(editar.getIdProducte().getTitol());
        
    }//GEN-LAST:event_formWindowOpened

