package capa_model;

/**
 *
 * @author gerar
 */
public class Estil {
    private long ID;
    private String nom; 

    public Estil(long ID, String nom) {
        this.ID = ID;
        this.nom = nom;
    }
    
    
    
    
    
    public long getID() {
        return ID;
    }

    public void setID(long ID) {
        this.ID = ID;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }
    
    
}
